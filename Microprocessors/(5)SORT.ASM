.model small
.stack 100h
.data
D1 DW 21,2,3,34,5,46,7,8,9,0AH
.code
start:
MOV AX,@data
MOV DS,AX
MOV DL,09H
MOV CL,0AH

A1:
MOV DL,09H
MOV SI,OFFSET D1
MOV DI,SI
INC DI
INC DI
A2:
MOV AX,[SI]
MOV BX,[DI]
CMP AX,BX
JC A3
PUSH AX
PUSH BX
POP AX
POP BX
MOV [SI],AX
MOV [DI],BX
A3:
INC SI
INC SI
INC DI
INC DI
DEC DL
JNZ A2
DEC CL
JNZ A1

mov si,offset d1
MOV CL,0AH

A4:
MOV BP,[SI]
CALL OTP
INC SI
INC SI
DEC CL
JNZ A4

MOV AH,4CH
INT 21H

INP PROC NEAR
MOV BP,0H
MOV CH,04H
MOV CL,04H
L2:MOV AH,01H
INT 21H
SUB AL,30H
CMP AL,0AH
JC L1
SUB AL,07H
L1:XOR AH,AH
SHL BP,CL
ADD BP,AX
DEC CH
JNZ L2
RET
INP ENDP


OTP PROC NEAR
push cx
MOV CH,04H
MOV CL,0CH
L4: MOV DX,BP
SHR DX,CL
AND DX,0FH
ADD DL,30H
CMP DL,3AH
JC L3
ADD DL,07H
L3: MOV AH,02H
INT 21H
SUB CL,04H
DEC CH
JNZ L4
pop cx
RET
OTP ENDP


END

