.model small
.stack 100h
.data
D1 DB 0H
.code
start:
MOV AX,@data
MOV DS,AX
MOV SI,OFFSET D1
MOV CH,04H
A1:
CALL INP
MOV [SI],DL
INC SI
DEC CH
JNZ A1

MOV SI,OFFSET D1
MOV CH,04H
MOV DH,00H
A2:
MOV DL,[SI]
MOV AH,02H
INT 21H
INC SI
DEC CH
JNZ A2

MOV AH,4CH
INT 21H

INP PROC NEAR
MOV DL,00H
MOV CL,08H
L2:
MOV AH,01H
INT 21H
RCR AL,1
RCL DL,1
DEC CL
JNZ L2
RET
INP ENDP


END

