.model small
.stack 100h
.data
.code
start:
MOV AX,@data
MOV DS,AX
CALL INP
MOV AX,BP
MOV DX, 00H
MOV BX,10H

L:
RCR AX,01H
JNC A1
INC DX
A1:
DEC BX
CMP BX,0H
JNZ L
MOV BP,DX
CALL OTP

MOV AH,4CH
INT 21H

INP PROC NEAR
MOV BP,0H
MOV CH,04H
MOV CL,04H
L2:MOV AH,01H
INT 21H
SUB AL,30H
CMP AL,0AH
JC L1
SUB AL,07H
L1:XOR AH,AH
SHL BP,CL
ADD BP,AX
DEC CH
JNZ L2
RET
INP ENDP


OTP PROC NEAR
MOV CH,04H
MOV CL,0CH
L4: MOV DX,BP
SHR DX,CL
AND DX,0FH
ADD DL,30H
CMP DL,3AH
JC L3
ADD DL,07H
L3: MOV AH,02H
INT 21H
SUB CL,04H
DEC CH
JNZ L4
RET
OTP ENDP


END

